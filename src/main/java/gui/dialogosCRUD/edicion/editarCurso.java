    public editarCurso(java.awt.Frame parent, boolean modal, Curso cursoviejo) throws SQLException {
        super(parent, modal);
        initComponents();
        
        this.cursoviejo = cursoviejo;
        
        PreparedStatement st = logica.LogicaConexion.getConexion()
                .prepareStatement(sql.Sentencias.getCICLOS_CONCATENADOS());
        ResultSet rs = st.executeQuery();
        ArrayList<String> listaCiclos = new ArrayList();

        while (rs.next()) {
            listaCiclos.add(rs.getString(1));
        }

        comboCiclo.setModel(new DefaultComboBoxModel(listaCiclos.toArray()));
        
        comboCiclo.setSelectedIndex(cursoviejo.getIdCiclo()-1);
        comboTurno.setSelectedIndex(cursoviejo.getIdTurno()-1);
        campoCodigo.setText(cursoviejo.getCodigo());
        comboNivel.setSelectedIndex(cursoviejo.getNivel()-1);
        
        
    }

