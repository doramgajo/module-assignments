    public editarProfesor(java.awt.Frame parent, boolean modal, Profesor profantiguo) throws SQLException {
        super(parent, modal);
        initComponents();
        System.out.println(profantiguo.toString());
        
        this.profantiguo = profantiguo;
        
        spinnerHorasContratadas.setValue(profantiguo.getHorasContratadas());
              
        spinnerOrden.setValue(profantiguo.getNumeroOrden());
        campoNombre.setText(profantiguo.getNombre());
        campoApellido.setText(profantiguo.getApellido());
        spinnerOrden.setValue(profantiguo.getNumeroOrden());
        
        PreparedStatement st = logica.LogicaConexion.getConexion()
                .prepareStatement(sql.Sentencias.getESPECIALIDADES_CONCATENADAS());
        ResultSet rs = st.executeQuery();
        ArrayList<String> listaEspes = new ArrayList();

        while (rs.next()) {
            listaEspes.add(rs.getString(1));
        }

        
        comboEspecialidad.setModel(new DefaultComboBoxModel(listaEspes.toArray()));
        for (int i = 0; i < listaEspes.size(); i++) {
            System.out.println(i + ": lista: " + listaEspes.get(i).substring(0, listaEspes.get(i).indexOf(".")));
            System.out.println(i + ": moduloviejo: " + profantiguo.getIdEspecialidad());
            if (listaEspes.get(i).substring(0, listaEspes.get(i).indexOf(".")).equals(String.valueOf(profantiguo.getIdEspecialidad()))) {
                comboEspecialidad.setSelectedIndex(i);
                System.out.println("coincide");
            } else {
                System.out.println("no coincide");
            }
        }
        
    }

