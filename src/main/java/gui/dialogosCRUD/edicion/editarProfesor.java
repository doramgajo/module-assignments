    private void botonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearActionPerformed
        if (campoNombre.getText().isBlank() || campoApellido.getText().isBlank()){
            JOptionPane.showMessageDialog(null, "Los campos no pueden estar vacíos.");
        } else {
            try {
                Profesor profe = new objetos.Profesor(
                                campoNombre.getText(),
                                campoApellido.getText(),
                                Integer.parseInt(spinnerOrden.getValue().toString()),
                                Integer.parseInt(comboEspecialidad.getSelectedItem().toString().substring(0, comboEspecialidad.getSelectedItem().toString().indexOf("."))),
                                Integer.parseInt(spinnerHorasContratadas.getValue().toString())                        
                        );
                System.out.println(profe.toString());
                crud.ProfesoresCRUD.actualizar(profe, String.valueOf(profantiguo.getIdProfesor()));
                dispose();
                logica.LogicaTablas.actualizarTablasBaseDeDatos();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_botonCrearActionPerformed

